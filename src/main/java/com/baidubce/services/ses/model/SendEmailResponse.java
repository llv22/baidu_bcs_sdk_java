package com.baidubce.services.ses.model;

import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * Response object of sending email. It will be returned while sending email successfully.
 */
public class SendEmailResponse extends SesResponse {
    /**
     * The internal identify.
     */
    @JsonProperty("message_id")
    private String messageId;

    public String getMessageId() {
        return messageId;
    }

    public void setMessageId(String messageId) {
        this.messageId = messageId;
    }

    @Override
    public String toString() {
        return "SendEmailResponse [messageId=" + messageId + "]";
    }

}
